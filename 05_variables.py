"""
Program: Demonstrate how to create variables in Python
Author: ChatGPT (Example)
Description:
    This program shows the creation of variables of different data types,
    including integer, float, string, boolean, list, tuple, dictionary, and set.
"""

# 1️⃣ Numeric variables
age = 25                  # Integer
height = 5.9              # Float

# 2️⃣ Text variable
name = "Alice"            # String

# 3️⃣ Boolean variable
is_student = True         # Boolean

# 4️⃣ Collection variables
fruits = ["apple", "banana", "cherry"]   # List (mutable)
coordinates = (10.5, 20.3)               # Tuple (immutable)
person = {"name": name, "age": age}      # Dictionary (key-value pairs)
unique_numbers = {1, 2, 3, 3, 4}         # Set (unique elements)

# 5️⃣ Display the variables
print("=== Created Variables in Python ===\n")
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is Student:", is_student)
print("Fruits:", fruits)
print("Coordinates:", coordinates)
print("Person Info:", person)
print("Unique Numbers:", unique_numbers)
